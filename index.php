<?php

namespace Syltaen\Controllers;

// use Syltaen\App\Services\Routes;

// ==================================================
// > 404
// ==================================================
if (is_404()) {
	( new Page() )->error404();


// ==================================================
// > SINGLES
// ==================================================
 } elseif (is_single()) {

	( new Page() )->single();

// ==================================================
// > HOMEPAGE
// ==================================================
 } elseif ( is_home() || is_front_page() ) {

	( new Page() )->home();

// ==================================================
// > PAGES
// ==================================================
 } else {

	( new Page() )->page();

 }
