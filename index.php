<?php

namespace Syltaen;

// ==================================================
// > 404
// ==================================================
if (is_404()) :

	( new PageController(false) )->render("404");


// ==================================================
// > SINGLES
// ==================================================
elseif (is_single()) :

	new SingleController( $post->post_type );


// ==================================================
// > HOMEPAGE
// ==================================================
elseif ( is_home() || is_front_page() ) :

	new HomeController();


// ==================================================
// > PAGES
// ==================================================
else :

	new PageController();


endif;
